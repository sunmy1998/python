# 数据类型和变量

# 整数 int
print("\n===============整数===============")
i = 1
print("i => ", i, " | type(i) => ", type(i))

# 浮点数 float
print("\n===============浮点数===============")
d = 3.14
print("d => ", d, " | type(d) => ", type(d))

# 字符串 str
print("\n===============字符串===============")
s = "I\'m OK!"
print("s => ", s, " | type(s) => ", type(s))

# 布尔值 bool
print("\n===============布尔值===============")
b = True
print("b => ", b, " | type(b) => ", type(b))
print("!b => ", not b)